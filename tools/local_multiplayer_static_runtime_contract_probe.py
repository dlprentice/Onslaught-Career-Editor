def contains_token(text: str, token: str) -> bool:
    return token in text or token.replace("\\", "\\\\") in text


def should_skip_historical_readiness_placeholder(label: str, token: str) -> bool:
    if "readiness" not in label.lower():
        return False
    return token == "redacted PID" or "<runtime-" in token or "runtime-" in token and "-redacted" in token


def check_tokens(path: Path, label: str, tokens: tuple[str, ...], failures: list[str]) -> None:
    text = read_text(path)
    for token in tokens:
        if should_skip_historical_readiness_placeholder(label, token):
            continue
        require(contains_token(text, token), f"{label} missing token: {token}", failures)
    lower = text.lower()
    for bad in OVERCLAIM_TOKENS:
        require(bad not in lower, f"{label} overclaim token present: {bad}", failures)


def check_table_row_tokens(path: Path, label: str, row_label: str, tokens: tuple[str, ...], failures: list[str]) -> None:
    lines = read_text(path).splitlines()
    prefix = f"| {row_label} |"
    rows = [line for line in lines if line.startswith(prefix)]
    require(len(rows) == 1, f"{label} expected one table row for {row_label}, found {len(rows)}", failures)
    if not rows:
        return
    row = rows[0]
    for token in tokens:
        require(contains_token(row, token), f"{label} {row_label} row missing token: {token}", failures)


def check_absent_tokens(path: Path, label: str, tokens: tuple[str, ...], failures: list[str]) -> None:
    text = read_text(path)
    for token in tokens:
        require(not contains_token(text, token), f"{label} stale/forbidden token present: {token}", failures)


